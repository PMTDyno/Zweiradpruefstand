  private void abort()
  {
    if(JOptionPane.showConfirmDialog(this, "Sind Sie sicher?", "Messung abbrechen", JOptionPane.YES_NO_OPTION) == 0)
    {
      gui.abortMeasurement();
      dispose();
    }
  }

  private void finish()
  {
    gui.finishMeasurement();
    dispose();
  }

